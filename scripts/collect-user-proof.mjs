/**
 * Kampanya kontratıyla etkileşime giren benzersiz cüzdanları zincirden toplar.
 *
 * "10+ gerçek kullanıcı" gereksiniminin doğrulanabilir kanıtını üretir:
 * her cüzdan, işlem hash'i ve tutarı Stellar Expert üzerinden kontrol edilebilir.
 *
 * Kullanım:
 *   node scripts/collect-user-proof.mjs            # markdown tablo -> docs/USER_PROOF.md
 *   node scripts/collect-user-proof.mjs --json     # ham JSON (stdout)
 */
import { writeFileSync } from "node:fs";
import { rpc, scValToNative } from "@stellar/stellar-sdk";

const CAMPAIGN_CONTRACT_ID = "CCUMBJRRHPBC6XRGCSK54NPY2IZ4EQGHAOL4B7XT5BUCHUYQKG2CLMUT";
const RPC_URL = "https://soroban-testnet.stellar.org";
const OUT = new URL("../docs/USER_PROOF.md", import.meta.url).pathname.replace(
  /^\/([A-Za-z]:)/,
  "$1"
);

const STROOPS = 10_000_000n;
const toXlm = (stroops) => {
  const whole = stroops / STROOPS;
  const frac = (stroops % STROOPS).toString().padStart(7, "0").replace(/0+$/, "");
  return frac ? `${whole}.${frac}` : whole.toString();
};

const KINDS = {
  campaign_created: "created campaign",
  donation: "supported campaign",
  withdrawal: "withdrew funds",
  refund: "claimed refund",
};

async function main() {
  const server = new rpc.Server(RPC_URL);
  const health = await server.getHealth();

  // RPC retention penceresini birden fazla dilimde tara
  const windowSize = 9_000;
  const events = [];
  let cursorStart = Math.max(health.latestLedger - windowSize * 4, health.oldestLedger + 1);

  while (cursorStart < health.latestLedger) {
    try {
      const page = await server.getEvents({
        startLedger: cursorStart,
        filters: [{ type: "contract", contractIds: [CAMPAIGN_CONTRACT_ID] }],
        limit: 200,
      });
      events.push(...page.events);
    } catch (error) {
      console.warn(`window ${cursorStart} skipped: ${error.message ?? error}`);
    }
    cursorStart += windowSize;
  }

  const interactions = [];
  for (const event of events) {
    try {
      const topics = event.topic.map((t) => scValToNative(t));
      const kind = KINDS[String(topics[0])];
      if (!kind) continue;
      const value = scValToNative(event.value);
      interactions.push({
        wallet: String(topics[1] ?? ""),
        kind,
        campaignId: Number(value.id ?? -1),
        amountXlm: value.amount !== undefined ? toXlm(BigInt(value.amount)) : null,
        ledger: event.ledger,
        txHash: event.txHash,
        at: event.ledgerClosedAt,
      });
    } catch {
      // parse edilemeyen olayları atla
    }
  }

  const byWallet = new Map();
  for (const interaction of interactions) {
    const existing = byWallet.get(interaction.wallet) ?? [];
    existing.push(interaction);
    byWallet.set(interaction.wallet, existing);
  }

  if (process.argv.includes("--json")) {
    console.log(JSON.stringify({ interactions, uniqueWallets: byWallet.size }, null, 2));
    return;
  }

  const rows = [...byWallet.entries()]
    .sort((a, b) => b[1].length - a[1].length)
    .map(([wallet, list], index) => {
      const kinds = [...new Set(list.map((i) => i.kind))].join(", ");
      const total = list
        .filter((i) => i.kind === "supported campaign")
        .reduce((sum, i) => sum + Number(i.amountXlm ?? 0), 0);
      const latest = list.sort((a, b) => b.ledger - a.ledger)[0];
      return `| ${index + 1} | [\`${wallet.slice(0, 8)}…${wallet.slice(-6)}\`](https://stellar.expert/explorer/testnet/account/${wallet}) | ${list.length} | ${kinds} | ${total || "—"} | [tx](https://stellar.expert/explorer/testnet/tx/${latest.txHash}) |`;
    });

  const markdown = `# On-chain proof of user interactions

Generated by \`scripts/collect-user-proof.mjs\` on ${new Date().toISOString()}.

Every row below is a distinct Stellar wallet that interacted with the FanFuel
campaign contract [\`${CAMPAIGN_CONTRACT_ID}\`](https://stellar.expert/explorer/testnet/contract/${CAMPAIGN_CONTRACT_ID})
on Stellar Testnet. All transactions are independently verifiable on Stellar Expert.

**Unique wallets: ${byWallet.size} · Total interactions: ${interactions.length}**

| # | Wallet | Interactions | Actions | Supported (XLM) | Latest tx |
|---|---|---|---|---|---|
${rows.join("\n")}

> Note: Soroban RPC only retains events for a limited window (~7 days). Older
> interactions remain valid on-chain and can be inspected on the contract page.
`;

  writeFileSync(OUT, markdown, "utf8");
  console.log(`unique wallets: ${byWallet.size}`);
  console.log(`interactions:   ${interactions.length}`);
  console.log(`written to:     docs/USER_PROOF.md`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
